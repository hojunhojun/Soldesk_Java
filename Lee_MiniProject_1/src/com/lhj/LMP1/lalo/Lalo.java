package com.lhj.LMP1.lalo;

public class Lalo {
	private String la;
	private String lo;
	
	public Lalo() {
		
	}
	
	public Lalo(String la, String lo) {
		super();
		this.la = la;
		this.lo = lo;
	}
	
	public String getLa() {
		return la;
	}
	public void setLa(String la) {
		this.la = la;
	}
	public String getLo() {
		return lo;
	}
	public void setLo(String lo) {
		this.lo = lo;
	}
	
}
